        <p><b>Cari resep takjil mudah dan murah? jika anda sedang mencari menu takjil buka puasa lainnya, es kopyor jawabannya </b></p> 
        <hr>
        <p style="text-indent: 45px;"> 
            Sesuai namanya, es kopyor adalah minuman yang disajikan dingin dengan isian daging kelapa kopyor dan campuran sirup serta susu kental manis.
            Kelapa kopyor merupakan bentuk tak normal dari buah kelapa, yang memiliki daging buah lebih lunak 
            serta cita rasa khas yang lebih manis dan gurih. Oleh sebab itu, es kopyor biasanya telah dimodifikasi 
            menggunakan ‘kopyor sintetis’, yang dibuat dari campuran agar-agar dan santan.
        </p>   
        <br>
        <p style="text-indent: 45px;">
            Tak perlu mengeluarkan modal yang besar, namun untuk rasa tentu akan menghasilkan
            kesegaran yang tak kalah dengan minuman lain. Dalam membuat es kopyor bisa siapkan bahan-bahan berikut ini:
        </p>
        <p>
            <ul>
                <li> Sirup cocopandan secukupnya </li>
                <li> Es batu 1 bongkah besar </li>
                <li> Agar-agar 1 bungkus warna putih </li>
                <li> Vanili 1/2 sendok teh </li>
                <li> Gula 4 sendok makan </li>
                <li> Santan kental 4 gelas </li>
                <li> Garam sedikit </li>
            </ul>
        </p> 
        <br>
        <p>
            Langkah pembuatannya :

            <ol>
                <li> Masukkan semua bahan hingga mendidih dan dinginkan sebentar jika sudah matang </li>
                <li> Guyur pelan-pelan ke atas es batu, biarkan menggumpal-gumpal, aduk sedikit demi sedikit hingga benar-benar keras</li>
                <li> Sajikan kopyor dengan sirup cocopandan dan es batu</li>
                <li> Dan Es Kopyor siap disantap </li>
            </ol>
        </p>
