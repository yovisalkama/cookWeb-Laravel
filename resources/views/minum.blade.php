                <li> 2 potong semangka, melon, strawberry </li>
                <li> air 500 mili liter</li>
                <li> 5 Sdm Susu Kental Manis</li>
                <li> 5 Sdm Sirup Cocopandan</li>
                <li> Es Batu Secukupnya</li>
