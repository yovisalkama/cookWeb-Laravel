        <h1>❀ Es Campur ❀</h1> 
        <img src="images/Es-Campur.jpg" style="display: block;"> 
        <hr> 
        <p><b>Siapa yang tidak mengenal minuman segar ini? rasanya di hampir semua 
              penjuru daerah sudah menjadi minuman sehari-hari untuk melepas dahaga. </b></p> 
        <hr>
        <p style="text-indent: 45px;"> 
            Saat bulan ramadhan apalagi, es campur salah satu minuman wajib dan menu takjil buka puasa yang tak boleh ketinggalan. Es campur 
            adalah salah satu minuman khas Indonesia yang cara membuatnya dengan mencampurkan berbagai jenis bahan dalam sirop manis. Bahan 
            yang dijadikan bahan biasanya berasa manis atau masam. Es campur dapat dijumpai di berbagai daerah di Indonesia dengan rasa dan bahan yang berbeda. 
            Oleh karena itu daerah asal dari es campur sulit ditentukan.
        </p>   
        <br>
        <p style="text-indent: 45px;">
            Cara membuatnya pun sangat mudah yang penting semua bahan tersedia lengkap. Dalam membuat es campur bisa disesuaikan dengan selera. 
            Sebagai referensi berikut bahan-bahan yang perlu disiapkan:
        </p>
        <p>
            <ul>
                <li> Buah kiwi, melon, strawberry, dan alpukat secukupnya. Bisa menggunakan buah yang disukai </li>
                <li> Susu kental manis 1 kaleng</li>
                <li> Simple Syrup 200 mili liter. Bisa juga menggunakan syrup yg sudah ada</li>
                <li> Nutrijell rasa leci 200 gram</li>
                <li> Kelapa muda 1 buah</li>
                <li> Air matang 1 liter </li>
                <li> Es batu secukupnya</li>
            </ul>
        </p> 
        <br>
        <p>
            Langkah pembuatannya :

            <ol>
                <li> Simple sirup : buat sirup simple nya dulu dengan perbandingan 2:1, 200 gram gula pasir dan 100 mili lier air. 
                     Rebus hingga mendidih dan gula benar-benar mencari, kemudian angkat dan dinginkan.</li>
                <li> Potong-potong buah dan jelly yang sudah dimasak sesuai selera</li>
                <li> Letakkan potongan buah, jelly dan air kalapa serta daging buah kepala dalam satu wadah</li>
                <li> Siram dengan susu kental manis sesuai selera atau pakai 1/2 kaleng saja dan beri simple syrup juga air</li>
                <li> Sajikan dingin lebih nikmat dengan mencapurnya menggunakan es batu</li>
            </ol>
        </p>
